package com.locadora_rdt_backend.modules.positions.logging;

import java.util.LinkedHashMap;
import java.util.Map;

public class PositionLogContext {

    private Long positionId;
    private String positionName;
    private String resource;
    private String operation;
    private String status;
    private String filterName;
    private String className;
    private String methodName;
    private Long durationMs;
    private Integer httpStatus;
    private String errorCode;
    private String exceptionName;
    private Boolean success;

    public Long getPositionId() {
        return positionId;
    }

    public void setPositionId(Long positionId) {
        this.positionId = positionId;
    }

    public String getPositionName() {
        return positionName;
    }

    public void setPositionName(String positionName) {
        this.positionName = positionName;
    }

    public String getResource() {
        return resource;
    }

    public void setResource(String resource) {
        this.resource = resource;
    }

    public String getOperation() {
        return operation;
    }

    public void setOperation(String operation) {
        this.operation = operation;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getFilterName() {
        return filterName;
    }

    public void setFilterName(String filterName) {
        this.filterName = filterName;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public String getMethodName() {
        return methodName;
    }

    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }

    public Long getDurationMs() {
        return durationMs;
    }

    public void setDurationMs(Long durationMs) {
        this.durationMs = durationMs;
    }

    public Integer getHttpStatus() {
        return httpStatus;
    }

    public void setHttpStatus(Integer httpStatus) {
        this.httpStatus = httpStatus;
    }

    public String getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(String errorCode) {
        this.errorCode = errorCode;
    }

    public String getExceptionName() {
        return exceptionName;
    }

    public void setExceptionName(String exceptionName) {
        this.exceptionName = exceptionName;
    }

    public Boolean getSuccess() {
        return success;
    }

    public void setSuccess(Boolean success) {
        this.success = success;
    }

    public Map<String, Object> toAttributes() {
        Map<String, Object> attributes = new LinkedHashMap<>();

        attributes.put("positionId", positionId);
        attributes.put("positionName", positionName);
        attributes.put("filterName", filterName);
        attributes.put("httpStatus", httpStatus);
        attributes.put("errorCode", errorCode);
        attributes.put("success", success);

        return attributes;
    }
}